Visual Cycling explores and analyzes cycling race data from major cycling competitions with in the past five years. Specifically we explored data from the Kilometer, Individual Pursuit, Team Pursuit, and Team Sprint events. With this data we were able to find statistics to describe the events, like average lap time for an event, and overall country statistics. Visual Cycling however dug further to explore patterns and strategies of cyclists. These patters we found by analyzing lap position and the number of changes in lap position a cyclist makes using algorithms like apriori and k-means clustering.
